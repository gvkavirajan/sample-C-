using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace switchcase
{
    class Program
    {
        static void Main(string[] args)
        {
            int value = 5;
            switch (value)
            {
                case 1:
                    Console.WriteLine(1);
                    break;
                case 5:
                    Console.WriteLine(5);
                    break;
                    Console.ReadLine();
            }

        }
    }
}
